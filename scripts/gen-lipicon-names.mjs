// scripts/gen-lipicon-names.mjs
// Generates src/LiPicons.Blazor/LipiconName.cs — strongly-typed string constants for
// every icon name in dist/json/icons.json. Re-run after adding/removing icons:
//   node scripts/gen-lipicon-names.mjs
import fs from "node:fs";
import path from "node:path";

const root = path.resolve(path.dirname(new URL(import.meta.url).pathname), "..");
const manifest = JSON.parse(
  fs.readFileSync(path.join(root, "dist/json/icons.json"), "utf8"),
);
const names = Object.keys(manifest.icons).sort();

// kebab/snake/space → PascalCase, C#-identifier-safe.
function toPascal(name) {
  let id = name
    .split(/[^a-zA-Z0-9]+/)
    .filter(Boolean)
    .map((w) => w.charAt(0).toUpperCase() + w.slice(1))
    .join("");
  if (/^[0-9]/.test(id)) id = "_" + id; // identifiers can't start with a digit
  return id;
}

// Resolve any PascalCase collisions deterministically (e.g. "user-x" vs "user_x").
const used = new Map();
const members = [];
for (const name of names) {
  let id = toPascal(name);
  if (used.has(id)) {
    let n = 2;
    while (used.has(`${id}${n}`)) n++;
    id = `${id}${n}`;
  }
  used.set(id, name);
  members.push({ id, name });
}

const header = `// <auto-generated />
// Generated by scripts/gen-lipicon-names.mjs from dist/json/icons.json
// Manifest version ${manifest.version} · ${members.length} icons. Do not edit by hand.
namespace LiPicons.Blazor;

/// <summary>
/// Strongly-typed names for every icon in the manifest. Use these instead of raw
/// strings to get compile-time safety and IntelliSense, e.g.
/// <c>&lt;LipiIcon Name="@LipiconName.EmptyState" /&gt;</c>.
/// </summary>
public static class LipiconName
{
`;

const body = members
  .map((m) => `    /// <summary>"${m.name}"</summary>\n    public const string ${m.id} = "${m.name}";`)
  .join("\n\n");

const out = `${header}${body}\n}\n`;
fs.writeFileSync(path.join(root, "src/LiPicons.Blazor/LipiconName.cs"), out);
console.log(`✓ wrote LipiconName.cs with ${members.length} constants`);
